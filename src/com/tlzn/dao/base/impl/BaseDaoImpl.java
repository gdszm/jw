	 /**
     * 翻页统一处理方法
     * @param sql 查询语句
     * @return DataGrid 数据结果集合对象
     * @throws Exception
     */
    public DataGrid getPageGrid(String hql,BaseObject object)
    {
    	DataGrid j = new DataGrid();
    	if (object.getPage() < 1) {
    		object.setPage(1);//默认第一页
		}
		if (object.getRows() < 1) {
			object.setRows(10);//默认每页显示10条设录
		}
		
		//得到总记录数		
		try {
			Long totalRows = count("select count(*) "+hql);		
			j.setTotal(totalRows);
			
			Query q = this.getCurrentSession().createQuery(hql);
			//从那条记录开始取
			
			q.setFirstResult((object.getPage() - 1) * object.getRows());
			//从设置的开始取的条数向下取多少条
			q.setMaxResults(object.getRows());
			j.setRows(q.list());
		} catch (HibernateException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		return j;
	  }
    
